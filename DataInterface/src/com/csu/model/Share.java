package com.csu.model;

public class Share {
	private int share_number;
	private String share_txt;
	private int frequency;
	
	public int getShare_number() {
		return share_number;
	}
	public void setShare_number(int share_number) {
		this.share_number = share_number;
	}
	public String getShare_txt() {
		return share_txt;
	}
	public void setShare_txt(String share_txt) {
		this.share_txt = share_txt;
	}
	public int getFrequency() {
		return frequency;
	}
	public void setFrequency(int frequency) {
		this.frequency = frequency;
	}
	
}
